def lib_update(libf, type, file):
	""" Updates a macro link in the lib for a certain type """
	fd = open(libf, "r+")
	lib = eval(fd.read())
	fd.close()
	lib[type] = os.path.relpath(file, INCLUDE)
	fd = open(libf, "w+")
	fd.write(str(lib))
	fd.close()

